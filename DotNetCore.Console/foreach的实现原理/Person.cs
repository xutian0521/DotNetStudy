using System;

namespace DotNetCore.Console_.foreach的实现原理
{
    // Person类包括两个属性。  
    public class Person
    {
        public Person(string fName, string lName)  
        {  
            this.firstName = fName;  
            this.lastName = lName;  
        }  
        public string firstName { get; set;}
        public string lastName { get; set;}
    }
}
